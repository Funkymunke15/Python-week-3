# working with strings

string = "My name is Davis Arita"
# prints the length of the string
print(len(string))
# changes all lowercase a with upper case A
upperA = string.replace('a', 'A')
print(upperA)
# capitalize the whole string
print(string.upper())
# prints the second character
print(string[1])
# prints the last character
print(string[-1])
# prints the middle character
print(string[len(string)//2])
